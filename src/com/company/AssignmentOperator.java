package com.company;

public class AssignmentOperator {
    public static void main(String[] args) {
        //Assignment Operator
        int x = 10;
        x = x + 5;
        System.out.println(x);

        x = x - 5;
        System.out.println(x);

        x = x * 2;
        System.out.println(x);

        x = x / 5;
        System.out.println(x);

        x = x % 2;
        System.out.println(x);
    }
}
