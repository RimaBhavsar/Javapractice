package com.company;

public class ComparisonOp {
    public static void main(String[] args) {
        int x = 20;
        int y = 20;
        System.out.println(x==y);
        System.out.println(x!=y);
        int a = 25;
        int b = 30;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(x>=y);



    }
}
