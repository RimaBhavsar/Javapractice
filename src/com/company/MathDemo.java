package com.company;

public class MathDemo {
    public static void main(String[] args) {
        System.out.println("subtractExact:  "+Math.subtractExact(123,23));
        System.out.println("multiplyExact:   "+Math.multiplyExact(10,9));
        System.out.println("ceil:  "+Math.ceil(10));
        System.out.println("Maximum value: "+Math.max(123,456));
        System.out.println("min: "+Math.min(468,8644));
        System.out.println("Random: "+Math.random());
        System.out.println("PI: "+Math.PI);
        System.out.println("getExponent:  "+Math.getExponent(123));

        int myMin   =Math.min(274,263857);
        System.out.println(myMin);

        System.out.println("Square root: "+Math.sqrt(1000000));
        System.out.println("E:  "+Math.E);


    }
}
