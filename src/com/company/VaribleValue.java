package com.company;

public class VaribleValue {

    static int a ;
    static short b ;
    static boolean c ;
    static char d ;
    static long e ;
    static byte f ;
    static double g ;
    static float h ;
    static String i ;

    public static void main(String[] args) {

        System.out.println("This is int :" + a);
        System.out.println("This is short :" + b);
        System.out.println("This is boolean :" + c);
        System.out.println("This is char :" + d);
        System.out.println("This is long :" + e);
        System.out.println("This is byte :" + f);
        System.out.println("This is double :" + g);
        System.out.println("This is float :" + h);
        System.out.println("This is String  :" + i);
    }
    }

